);

CREATE TABLE IF NOT EXISTS exchange_rates (
  id VARCHAR(96) PRIMARY KEY,
  rate_date DATE NOT NULL,
  from_currency VARCHAR(3) NOT NULL,
  to_currency VARCHAR(3) NOT NULL DEFAULT 'USD',
  rate DECIMAL(18,8) NOT NULL,
  maintained_by_user_id VARCHAR(64) NOT NULL,
  source_note VARCHAR(500),
  metadata_json JSON,
  created_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
  updated_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  UNIQUE KEY uq_exchange_rates_pair_date (rate_date, from_currency, to_currency),
  INDEX idx_exchange_rates_maintainer (maintained_by_user_id),
  CONSTRAINT fk_exchange_rates_maintainer FOREIGN KEY (maintained_by_user_id) REFERENCES users(id)
);

CREATE TABLE IF NOT EXISTS price_decisions (
  id VARCHAR(96) PRIMARY KEY,
  request_item_id VARCHAR(96) NOT NULL,
  pas_quote_id VARCHAR(96),
  history_unit_price_usd DECIMAL(18,4),
  quote_unit_price_usd DECIMAL(18,4),
  unit_price_delta_usd DECIMAL(18,4),
  threshold_usd DECIMAL(18,4) NOT NULL DEFAULT 0.4000,
  decision_type VARCHAR(40) NOT NULL DEFAULT 'pending',
  decision_reason VARCHAR(1000),
  requires_dept_dri_review TINYINT(1) NOT NULL DEFAULT 0,
  requires_budget_approval TINYINT(1) NOT NULL DEFAULT 0,
  decided_by_user_id VARCHAR(64),
  decided_at TIMESTAMP NULL,
  metadata_json JSON,
  created_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
  updated_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  INDEX idx_price_decisions_item (request_item_id),
  INDEX idx_price_decisions_quote (pas_quote_id),
  INDEX idx_price_decisions_type (decision_type),
  CONSTRAINT fk_price_decisions_item FOREIGN KEY (request_item_id) REFERENCES request_items(id),
  CONSTRAINT fk_price_decisions_quote FOREIGN KEY (pas_quote_id) REFERENCES pas_quotes(id),
  CONSTRAINT fk_price_decisions_decider FOREIGN KEY (decided_by_user_id) REFERENCES users(id)
);

