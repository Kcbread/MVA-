);

CREATE TABLE IF NOT EXISTS pas_quotes (
  id VARCHAR(96) PRIMARY KEY,
  request_item_id VARCHAR(96) NOT NULL,
  entered_by_user_id VARCHAR(64) NOT NULL,
  pas_demand_no VARCHAR(120),
  pas_material_no VARCHAR(120),
  factory_material_no VARCHAR(120),
  vendor_name VARCHAR(200),
  quote_currency VARCHAR(3) NOT NULL,
  quote_unit_price DECIMAL(18,4) NOT NULL,
  quote_unit_price_usd DECIMAL(18,4),
  quote_date DATE NOT NULL,
  quote_received_date DATE NOT NULL,
  valid_until DATE,
  quote_status VARCHAR(40) NOT NULL DEFAULT 'received',
  screenshot_attachment_id VARCHAR(96),
  excel_attachment_id VARCHAR(96),
