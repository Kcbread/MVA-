-- MVA procurement workflow persistence target tables.
-- Apply after procurement-prototype/db/schema.sql.

CREATE TABLE IF NOT EXISTS schema_migrations (
  version VARCHAR(80) PRIMARY KEY,
  description VARCHAR(240) NOT NULL,
  applied_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE IF NOT EXISTS request_packages (
  id VARCHAR(96) PRIMARY KEY,
  package_code VARCHAR(120) NOT NULL UNIQUE,
  request_type VARCHAR(40) NOT NULL DEFAULT 'standard',
  created_by_user_id VARCHAR(64) NOT NULL,
  demand_department VARCHAR(120) NOT NULL,
  project_family VARCHAR(40),
  project_code VARCHAR(80),
  project_name VARCHAR(160),
  need_date DATE,
  status VARCHAR(40) NOT NULL DEFAULT 'draft',
  current_stage VARCHAR(80) NOT NULL DEFAULT 'requester_draft',
  pending_owner_role VARCHAR(40),
  pending_owner_user_id VARCHAR(64),
  submitted_at TIMESTAMP NULL,
  received_at TIMESTAMP NULL,
  stage_start_at TIMESTAMP NULL,
  closed_at TIMESTAMP NULL,
  metadata_json JSON,
  created_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
  updated_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  INDEX idx_request_packages_creator (created_by_user_id),
  INDEX idx_request_packages_pending_user (pending_owner_user_id),
  INDEX idx_request_packages_status_stage (status, current_stage),
  INDEX idx_request_packages_scope (project_family, project_code, demand_department),
  CONSTRAINT fk_request_packages_creator FOREIGN KEY (created_by_user_id) REFERENCES users(id),
  CONSTRAINT fk_request_packages_pending_user FOREIGN KEY (pending_owner_user_id) REFERENCES users(id)
);

CREATE TABLE IF NOT EXISTS request_items (
  id VARCHAR(96) PRIMARY KEY,
  request_package_id VARCHAR(96) NOT NULL,
  line_no INT NOT NULL,
  item_id VARCHAR(96),
  material_id VARCHAR(96),
  item_name VARCHAR(240) NOT NULL,
  item_spec TEXT,
  item_category VARCHAR(120),
  demand_type VARCHAR(40) NOT NULL DEFAULT 'mfg',
  total_requested_qty DECIMAL(18,4) NOT NULL DEFAULT 0,
  estimate_currency VARCHAR(3),
  estimate_unit_price DECIMAL(18,4),
  estimate_unit_price_usd DECIMAL(18,4),
  estimate_reason VARCHAR(500),
  status VARCHAR(40) NOT NULL DEFAULT 'draft',
  created_by_user_id VARCHAR(64) NOT NULL,
  metadata_json JSON,
  created_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
  updated_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  UNIQUE KEY uq_request_items_package_line (request_package_id, line_no),
  INDEX idx_request_items_package (request_package_id),
  INDEX idx_request_items_item (item_id),
  INDEX idx_request_items_material (material_id),
  INDEX idx_request_items_status (status),
  CONSTRAINT fk_request_items_package FOREIGN KEY (request_package_id) REFERENCES request_packages(id),
  CONSTRAINT fk_request_items_item FOREIGN KEY (item_id) REFERENCES item_master(id),
  CONSTRAINT fk_request_items_material FOREIGN KEY (material_id) REFERENCES material_identity(id),
  CONSTRAINT fk_request_items_creator FOREIGN KEY (created_by_user_id) REFERENCES users(id)
);

CREATE TABLE IF NOT EXISTS request_demand_lines (
  id VARCHAR(96) PRIMARY KEY,
  request_item_id VARCHAR(96) NOT NULL,
  line_no INT NOT NULL,
  demand_type VARCHAR(40) NOT NULL DEFAULT 'mfg',
  project_code VARCHAR(80),
  line_code VARCHAR(80),
  phase_code VARCHAR(40),
  station_or_unit VARCHAR(120) NOT NULL,
  quantity DECIMAL(18,4) NOT NULL DEFAULT 0,
  need_date DATE,
  remark VARCHAR(500),
  status VARCHAR(40) NOT NULL DEFAULT 'active',
  metadata_json JSON,
  created_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
  updated_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  UNIQUE KEY uq_request_demand_lines_item_line (request_item_id, line_no),
  INDEX idx_request_demand_lines_item (request_item_id),
  INDEX idx_request_demand_lines_phase_station (phase_code, station_or_unit),
  INDEX idx_request_demand_lines_scope (project_code, line_code, phase_code),
  CONSTRAINT fk_request_demand_lines_item FOREIGN KEY (request_item_id) REFERENCES request_items(id)
);

CREATE TABLE IF NOT EXISTS item_master_requests (
