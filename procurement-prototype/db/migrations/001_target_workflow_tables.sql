  created_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
  updated_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  INDEX idx_pas_quotes_item (request_item_id),
  INDEX idx_pas_quotes_entered_by (entered_by_user_id),
  INDEX idx_pas_quotes_demand_no (pas_demand_no),
  INDEX idx_pas_quotes_valid_until (valid_until),
